//deklarasikan jari jari lingkaran dan pi
let radius = 5;
const pi = 22 / 7;

//kalkulasikan jari jari dengan rumus diameter, keliling, dan luas lingkaran
let diameter = radius * 2;
let circumference = 2 * pi * radius;
let area = pi * radius * radius;

//output hasil
console.log(
  `lingkaran memiliki diameter ${diameter}, keliling ${circumference}, dan luas ${area}`
);
